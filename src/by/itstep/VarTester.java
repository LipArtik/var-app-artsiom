package by.itstep;

public class VarTester {

    public static void main(String[] args) {
        System.out.println("Hello world!");
        System.out.println("My name is Artsiom");

        System.out.print("Current t = -2\n");
        System.out.print("Tomorrow t =-6\n");

        //variable declaration -
        // <type> <var_name>;
        int temp;
        //variable initialisation - ввод данных в переменные
        //<var_name> = <value>;
        temp = -2;



        //variable declaration -
        //<type> <var_name>;

        double avg;

        //variable initialisation
        //<var_name> = <value>;
        avg = 7.8;
        // variable declaration & initialisation in single line
        // <type> <var_name> = <value>;
        int age = 23;
        int h = 191;
        int nextemp =-6;
        System.out.println(temp);
        System.out.println("Tomorrow temp is "+ nextemp);
        // <type> <var_name> = <value>,...;
        int a = 15, b = 30 ;
        int mathmark = 8, historyMark = 8 , SpanishMark = 7;
        System.out.println("My Mathmark is: "+mathmark);




    }


}
